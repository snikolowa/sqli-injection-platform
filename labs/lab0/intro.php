  <h2>1. Какво представлява SQL Injection?</h2>
  <p>
    SQL Injection (SQLi) е клас уязвимости в уеб приложения, при които нападателят успява да
    манипулира SQL заявки, изпълнявани от приложението, чрез въвеждане на специално подготвени
    входни данни. Това е възможно, когато потребителският вход не се обработва безопасно
    и се използва директно при изграждане на SQL заявка.
  </p>

  <h2>2. Как възниква SQL Injection?</h2>
  <p>
    Най-често SQL Injection възниква при използване на динамично изградени SQL заявки,
    където входът от потребителя се „слепва“ към SQL кода. В такива случаи приложението
    не прави ясно разграничение между SQL код и данни, което позволява промяна на логиката
    на заявката.
  </p>

  <h2>3. Защо SQL Injection е опасна?</h2>
  <p>
    При успешно изпълнение на SQL Injection атака, нападателят може да получи неоторизиран
    достъп до данни, да заобиколи механизми за автентикация, да модифицира или изтрие
    информация и в някои случаи да постигне пълен контрол върху базата данни.
  </p>

  <h2>4. Основни видове SQL Injection</h2>
  <p>
    В зависимост от начина на експлоатация и обратната връзка от приложението,
    SQL Injection атаките могат да бъдат класифицирани в няколко основни категории:
  </p>
  <ul>
    <li><strong>In-band SQL Injection</strong> – атакуващият използва същия комуникационен канал
        за изпращане на заявката и получаване на резултата.</li>
    <li><strong>Inferential (Blind) SQL Injection</strong> – информацията се извлича индиректно
        чрез логически условия или измерване на времето за отговор.</li>
    <li><strong>Out-of-Band SQL Injection</strong> – използва се алтернативен комуникационен канал
        за извличане на данни.</li>
  </ul>

  <h2>5. NoSQL Injection</h2>
  <p>
    Освен класическите релационни бази данни, уязвимости от тип Injection могат да възникнат
    и при NoSQL системи. При тях атаките се реализират чрез манипулиране на синтаксиса
    или използване на специфични оператори, характерни за конкретната NoSQL платформа.
  </p>

  <h2>6. Общи принципи за защита</h2>
  <ul>
    <li>Използване на подготвени заявки (prepared statements)</li>
    <li>Строга валидация на входните данни</li>
    <li>Минимални привилегии за достъп до базата данни</li>
    <li>Ограничаване на съобщенията за грешки към крайния потребител</li>
  </ul>

